    <h3>A Manifesto on Cassidy's Guiding Principles</h3>
    <p>
        Cassidy is a training platform inspired by the novel <i>Once a Runner</i> and named after its protagonist, Quenton Cassidy.
        <br> <br>
        Every training platform has a unique take on the best ways to think about training.
    </p>
    <ul>
        <li>Strava focuses on the social aspect.</li>
        <li>Runalyze focuses on numerical analysis.</li>
        <li>Sites like Final Surge and Training Peaks also provide analysis, by also have strengths that relate coaches and athletes.</li>
    </ul>

    <p>Each of these have their strengths and weaknesses. Cassidy seeks to fill a slighly different niche.</p>
    <p>
        As in the thesis of <i>Once a Runner</i>, the guiding principle of Cassidy is that hardwork and dedication are the things that make better athletes.
        It's true that complex workout analysis, TRIMP, V02 Max, Lactate Threshold, and a whole host of physiological measures are all interesting and provide insight.
        However, the truth is that training is far more simple than sport physiologists would like to believe.
        All too often, we seek the 1% improvement or spend time fretting over which heartrate zone most of the run was spent in, all at the expense real development.

        With that being said, don't expect many fancy features or analysis tools.
        Cassidy takes the opinionated stance that these kinds of things detract and distract from what training is about.
        What matters is <i>"the trial of miles, miles of trials"</i>.
    </p>

    <p>
      Separate from training principles, Cassidy's other main goal is to provide a way to return data ownership to users.
      This is ultimately a much longer term vision for the project as a whole.

      True data ownership will only be accomplished in a meaningful way when users can choose where their fitness tracking devices sync to.
      That is, Garmin devices (or any other brand), should not have to go through Garmin's servers to sync data with Strava (or any other platform).
      It should be up to the users.
    </p>

