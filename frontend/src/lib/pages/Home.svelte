                I've used platforms like Strava, Runalyze and Final Surge.
                They all work well, but overtime I found myself increasingly wanting something different.
                Strava has slowly been locking its features away behind a paywall that I just am not willing to pay for.
                I appreciated Runalyze for a while, but as I've mentioned, I no longer think that heavy numerical analysis really helps all that much.
                Moreover, I wanted something a bit more intuitive to use.
                Finally, Final Surge comes the closest to the thing that I imagine the perfect training platform would look like.
                To that end, those familiar with Final Surge will see lots of similarities, and in many cases I take inspiration directly from it.
                For example, Cassidy's training calendar is identical in many ways to the Final Surge Calendar.
                If my current trajectory continues, I'll probably return to a simple pen and paper training log. But for now, I think Cassidy is the next step to simplification.
                (Note that Final Surge also provides a set of coach-athlete features that I have no interest in replicating)
                <br><br>
                On the topic of data ownership, my own hope is that eventually, anyone who wants to will be able to sync their personal wearables directly to any platform of their choosing.
                Toward this end, I hope that I can slowly develop Cassidy to be a local intermediary between wearables and other platforms (as well as a platform in its own right).
                At first, this is going to look like simple integrations with platforms like Strava.
                But ultimately, I have this (perhaps naive) dream that I will be able to sync my watch directly to Cassidy (and my personal computer), entirely circumventing the need for other servers.
                It's my belief that Garmin, Coros, Suunto, Polar et al. should be primarly watch companies and not data distributors.
                That task should fall to users who can decide where their data goes (and if they so choose, it can go to the watch companies' servers).
